// 1칸을 뛸 땐 1가지
// 2칸을 뛸 땐 2가지
// 3칸을 뛸 땐 3가지
// 4칸을 뛸 땐 5가지
// 5칸을 뛸 땐 1,1,1,1,1 / 2,1,1,1 / 1,2,1,1 / 1,1,2,1 / 1,1,1,2
// /2,2,1 / 1,2,2 / 2,1,2 로 8가지.
// 1,2,3,5,8 ... 패턴이 있어보인다.
// 피보나치다.

function solution(n) {
    const arr = [1,1];
    
    for(let i=2; i<=n; i++) {
        arr[i] = (arr[i-1] + arr[i-2]) % 1234567;
    }
   return arr[n];
}